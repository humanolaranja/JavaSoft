    public void setVisible(boolean bool, Loja l){
        this.setVisible(bool);
        this.loja = l;
        this.preencherTabela();        
        
        this.jlNome.setText(loja.getNome());
        this.jlID.setText(String.valueOf(loja.getId()));
        this.jlAluguel.setText("R$" + String.valueOf(loja.getValorAluguel()));
        this.jlAbertura.setText(String.valueOf(loja.getHorarioAbertura()));
        this.jlFechamento.setText(String.valueOf(loja.getHorarioFechamento()));
    }
    
    public void preencherTabela(){
        DefaultTableModel val = (DefaultTableModel) this.jTable1.getModel();
        val.setRowCount(0);
        
        for(Funcionario f : this.loja.getFuncionarios()){
            val.addRow(new String[]{f.getPrimeiroNome() + " " + f.getSobrenome(), f.getCpf(), f.getTurnoString(), "R$"+String.format("%.2f", f.getSalario())});
        }
    }

