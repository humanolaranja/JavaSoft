    public void preencherTabela(){
        String [] colunas = new String [] {"Nome", "Turno", "Idade"}; 
        ArrayList dados = new ArrayList();
        funcionarios.forEach((f) -> {
            dados.add(new Object[]{f.getPrimeiroNome(), f.getTurno(), f.getIdade()});
        });
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        
        tableFuncionarios.setModel(modelo);
        tableFuncionarios.setRowHeight(21);
        tableFuncionarios.getColumnModel().getColumn(0).setPreferredWidth(300);
        tableFuncionarios.getColumnModel().getColumn(0).setResizable(false);
        tableFuncionarios.getColumnModel().getColumn(1).setPreferredWidth(75);
        tableFuncionarios.getColumnModel().getColumn(1).setResizable(false);
        tableFuncionarios.getColumnModel().getColumn(2).setPreferredWidth(tableFuncionarios.getWidth()-375);
        tableFuncionarios.getColumnModel().getColumn(2).setResizable(false);
        tableFuncionarios.getTableHeader().setReorderingAllowed(false);
        tableFuncionarios.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tableFuncionarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

